<?php

namespace StarkBank;
use StarkBank\Utils\Resource;
use StarkBank\Utils\Checks;
use StarkBank\Utils\Rest;
use StarkBank\Utils\API;
use StarkBank\Transfer;


class TransferLog extends Resource
{
    /**
    # TransferLog object

    Every time a Transfer entity is modified, a corresponding TransferLog
    is generated for the entity. This log is never generated by the
    user.

    ## Attributes:
        - id [string]: unique id returned when the log is created. ex: "5656565656565656"
        - transfer [Transfer]: Transfer entity to which the log refers to.
        - errors [list of strings]: list of errors linked to this BoletoPayment event.
        - type [string]: type of the Transfer event which triggered the log creation. ex: "processing" or "success"
        - created [DateTime]: creation datetime for the transfer.
     */
    function __construct(array $params)
    {
        parent::__construct($params["id"]);
        unset($params["id"]);
        $this->created = Checks::checkDateTime($params["created"]);
        unset($params["created"]);
        $this->type = $params["type"];
        unset($params["type"]);
        $this->errors = $params["errors"];
        unset($params["errors"]);
        $this->transfer = $params["transfer"];
        unset($params["transfer"]);

        Checks::checkParams($params);
    }

    /**
    # Retrieve a specific TransferLog

    Receive a single TransferLog object previously created by the Stark Bank API by passing its id

    ## Parameters (required):
        - id [string]: object unique id. ex: "5656565656565656"

    ## Parameters (optional):
        - user [Project object]: Project object. Not necessary if starkbank.user was set before function call

    ## Return:
        - TransferLog object with updated attributes
     */
    public function get($user, $id)
    {
        return Rest::getId($user, TransferLog::resource(), $id);
    }

    /**
    # Retrieve TransferLogs

    Receive a generator of TransferLog objects previously created in the Stark Bank API

    ## Parameters (optional):
        - limit [integer, default null]: maximum number of objects to be retrieved. Unlimited if null. ex: 35
        - transfer_ids [list of strings, default null]: list of Transfer ids to filter retrieved objects. ex: ["5656565656565656", "4545454545454545"]
        - types [list of strings, default null]: filter retrieved objects by types. ex: "success" or "failed"
        - after [DateTime, default null] date filter for objects created only after specified date.
        - before [DateTime, default null] date filter for objects only before specified date.
        - user [Project object, default null]: Project object. Not necessary if starkbank.user was set before function call
    
    ## Return:
        - list of TransferLog objects with updated attributes
     */
    public function query($user, $options = [])
    {
        $options["after"] = Checks::checkDateTime($options["after"]);
        $options["before"] = Checks::checkDateTime($options["before"]);
        return Rest::getList($user, TransferLog::resource(), $options);
    }

    private function resource()
    {
        $transferLog = function ($array) {
            $transfer = function ($array) {
                return new Transfer($array);
            };
            $array["transfer"] = API::fromApiJson($transfer, $array["transfer"]);
            return new TransferLog($array);
        };
        return [
            "name" => "TransferLog",
            "maker" => $transferLog,
        ];
    }
}
